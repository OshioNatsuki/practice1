package com.example.practice1.ex;

public class Ex111Calculation {
    
    static int addition(int num1 ,int num2){
        return num1 + num2;
    }

    static int sub(int num1 ,int num2){
        return num1 - num2;
    }

    static int multi(int num1, int num2){
        return num1 * num2;
    }

    static int div(int num1, int num2){
        return num1 / num2;
    }

    static int remainder(int num1, int num2){
        return num1 % num2;
    }
}
